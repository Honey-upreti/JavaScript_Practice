// --- 1. Creating a Multidimensional Array ---

// A multidimensional array is an array that contains other arrays as its elements.
// In this example, we create a 3x3 array to represent a Tic-Tac-Toe board.
// Each inner array represents a row on the board.
const ticTacToeBoard = [
  ['X', 'O', 'X'], // This is the first row (index 0)
  ['O', 'X', 'O'], // This is the second row (index 1)
  ['O', 'O', 'X']  // This is the third row (index 2)
];

console.log('Original Board:');
console.log(ticTacToeBoard);
