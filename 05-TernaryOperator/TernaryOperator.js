// A simple example using a boolean condition
let isRaining = true;

// The ternary operator checks if 'isRaining' is true.
// If it is, the value 'Take an umbrella.' is assigned to the 'weatherMessage' variable.
// If it is false, the value 'Leave your umbrella at home.' is assigned instead.
let weatherMessage = isRaining ? 'Take an umbrella.' : 'Leave your umbrella at home.';
console.log('weatherMessage');
